// Premium Success/Error Toast notification engine
window.showToast = function(message, type = 'success') {
  let center = document.getElementById('toast-center');
  if (!center) {
    center = document.createElement('div');
    center.id = 'toast-center';
    center.className = 'toast-notification-center';
    document.body.appendChild(center);
  }

  const toast = document.createElement('div');
  toast.className = `toast-box ${type === 'error' ? 'toast-error' : (type === 'info' ? 'toast-info' : '')}`;

  const icon = type === 'error' ? '🚫' : (type === 'info' ? 'ℹ️' : '✅');

  toast.innerHTML = `
    <span>${icon}</span>
    <span style="font-size: 0.92rem; font-weight: 600; line-height: 1.4;">${message}</span>
    <span class="toast-close">&times;</span>
  `;

  center.appendChild(toast);

  toast.querySelector('.toast-close').addEventListener('click', () => {
    toast.remove();
  });

  setTimeout(() => {
    if (toast.parentNode) {
      toast.style.transition = 'all 0.35s cubic-bezier(0.16, 1, 0.3, 1)';
      toast.style.transform = 'translateX(120%)';
      toast.style.opacity = '0';
      setTimeout(() => toast.remove(), 350);
    }
  }, 4500);
};

// Override native alerts to avoid boxy interfaces
window.alert = function(msg) {
  if (typeof msg === 'string' && msg.trim() !== '') {
    const isErr = msg.toLowerCase().includes('error') || msg.toLowerCase().includes('fail') || msg.toLowerCase().includes('required') || msg.toLowerCase().includes('full') || msg.toLowerCase().includes('log');
    window.showToast(msg, isErr ? 'error' : 'info');
  }
};

// Injects standard service worker triggers
function registerPwaWorker() {
  if ('serviceWorker' in navigator) {
    navigator.serviceWorker.register('/sw.js')
      .then(reg => {
        console.log('[PWA] VUU Service Worker registered at scope:', reg.scope);
      })
      .catch(err => {
        console.error('[PWA] VUU Service Worker setup failed:', err);
      });
  }
}

